# 컨테이너 자료형 - [1] 순서있는 자료형 List
# 다양한 종류의 여러 데이터 저장 가능
# 데이터 추가/삭제/변경 가능
# 생성 문법 : 변수명 = [데이터1, 데이터2, ...]
#            변수명 = list([데이터1, 데이터2, ...])

# 5과목 점수 저장
grade1 = [90,89,100,79,99]

grade2 = list([90,89,100,79,99])

print(grade1)
print(grade2, '\n')

