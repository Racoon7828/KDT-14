## 11. 입력된 문자열의 코드값을 16진수로 출력하세요.
#  [ 예  시 ]
#  [ 입력 ] 메시지 입력 : Hello
#  [ 출력 ] Hello의 코드값 : 0x480x650x6c0x6c0x6f
# 모르겠다
print('\n\n#----------------------------------')
print('# 11번')
print('#----------------------------------')
msg = input("메시지 입력 : ")
mapping = {chr(i): i for i in range(1114112)}
hex = "0123456789abcdef"
result = ""
for i in msg:
    code = mapping[i]
    h = ""
    while code > 0:
        h = hex[code % 16] + h
        code //= 16
    result += "0x" + (h if h else "0")
print(f"{msg}의 코드값 : {result}")

